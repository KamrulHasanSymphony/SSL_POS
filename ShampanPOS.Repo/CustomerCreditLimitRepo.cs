        public ResultVM GetGridData(GridOptions options, string CustomerId)
        {
            try
            {
                HttpRequestHelper httpRequestHelper = new HttpRequestHelper();
                AuthModel authModel = new AuthModel { token = ClaimNames.token };

                #region Invoke API 

                // Add salePersonId as a query parameter, send options in the request body
                var data = httpRequestHelper.PostData($"api/CustomerCreditLimit/GetGridData?CustomerId={CustomerId}", authModel, JsonConvert.SerializeObject(options,
                    new JsonSerializerSettings
                    {
                        NullValueHandling = NullValueHandling.Ignore
                    }));

                ResultVM result = JsonConvert.DeserializeObject<ResultVM>(data);

                #endregion

                return result;
            }
            catch (Exception e)
