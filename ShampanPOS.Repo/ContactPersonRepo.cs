        public ResultVM GetGridData(GridOptions options)
        {
            try
            {
                HttpRequestHelper httpRequestHelper = new HttpRequestHelper();
                AuthModel authModel = new AuthModel { token = ClaimNames.token };

                // Log serialized options for debugging
                string payload = JsonConvert.SerializeObject(options, new JsonSerializerSettings { NullValueHandling = NullValueHandling.Ignore });
                Console.WriteLine("Request Payload: " + payload);

                // Invoke API
                var data = httpRequestHelper.PostData("api/ContactPerson/GetGridData", authModel, payload);

                // Log raw response for debugging
                Console.WriteLine("Response: " + data);

                // Deserialize and return result
                ResultVM result = JsonConvert.DeserializeObject<ResultVM>(data);
                return result;
            }
            catch (Exception e)
            {
                Console.WriteLine($"Error: {e.Message}");
                throw;
            }
        }


